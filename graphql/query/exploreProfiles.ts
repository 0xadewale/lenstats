import {basicClient} from "../initClient";
import Profile from "../../types/Profile";

export const exploreProfilesQuery = `
query ExploreProfiles($sortCriteria: ProfileSortCriteria!) {
  exploreProfiles(request: { sortCriteria: $sortCriteria }) {
    items {
      id
      name
      bio
      isDefault
      attributes {
        displayType
        traitType
        key
        value
      }
      followNftAddress
      metadata
      handle
      picture {
        ... on NftImage {
          contractAddress
          tokenId
          uri
          chainId
          verified
        }
        ... on MediaSet {
          original {
            url
            mimeType
          }
        }
      }
      coverPicture {
        ... on NftImage {
          contractAddress
          tokenId
          uri
          chainId
          verified
        }
        ... on MediaSet {
          original {
            url
            mimeType
          }
        }
      }
      ownedBy
      dispatcher {
        address
        canUseRelay
      }
      stats {
        totalFollowers
        totalFollowing
        totalPosts
        totalComments
        totalMirrors
        totalPublications
        totalCollects
      }
      followModule {
        ... on FeeFollowModuleSettings {
          type
          contractAddress
          amount {
            asset {
              name
              symbol
              decimals
              address
            }
            value
          }
          recipient
        }
        ... on ProfileFollowModuleSettings {
        type
        }
        ... on RevertFollowModuleSettings {
        type
        }
      }
    }
    pageInfo {
      prev
      next
      totalCount
    }
  }
}
`

async function exploreProfiles (sortCriteria: string): Promise<Profile[]>  {
    const response = await basicClient.query(exploreProfilesQuery, {
        sortCriteria
    }).toPromise()
    return response.data.exploreProfiles.items as Profile[]
}

export default exploreProfiles;
